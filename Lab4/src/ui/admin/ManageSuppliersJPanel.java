    void refreshTable() {
       populateTable();
    }

    private void populateTable() {
    
        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0);

        for (Supplier s : supplierDirectory.getSupplierList()) {
        
            Object[] row = new Object[1]; // only one column
        
            row[0] = s; // store the actual Supplier object
            
            model.addRow(row);
       }
    }

    
    
