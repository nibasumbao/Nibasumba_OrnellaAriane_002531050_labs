    private void btnViewSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewSupplierActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = tblProducts.getSelectedRow();
      if (selectedRow >= 0) {
        
        Supplier selectedSupplier = (Supplier) tblProducts.getValueAt(selectedRow, 0);
        
        ViewSupplierDetailJPanel panel = new ViewSupplierDetailJPanel(workArea, supplierDirectory, selectedSupplier);
        workArea.add("ViewSupplierJPanel", panel);
        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.next(workArea);
    
      } else {
    
        JOptionPane.showMessageDialog(this, "Please select a supplier first.", "Warning", JOptionPane.WARNING_MESSAGE);
    }

        
    }//GEN-LAST:event_btnViewSupplierActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        
                                               
       int selectedRow = tblProducts.getSelectedRow();

    if (selectedRow < 0) {
        JOptionPane.showMessageDialog(this, "Please select a supplier to remove.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Supplier selectedSupplier = (Supplier) tblProducts.getValueAt(selectedRow, 0);
    int dialogResult = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete " + selectedSupplier.getSupplierName() + "?",
            "Warning", JOptionPane.YES_NO_OPTION);

    if (dialogResult == JOptionPane.YES_OPTION) {
        supplierDirectory.removeSupplier(selectedSupplier);
        refreshTable();
        JOptionPane.showMessageDialog(this, "Supplier removed successfully.");
    }
 

    }//GEN-LAST:event_btnRemoveActionPerformed

