    private void populateRoleCombo() {
        cmbRoles.removeAllItems();
        cmbRoles.addItem("Administrator");
        cmbRoles.addItem("Supplier");
    
    }

    public void populateSupplierCombo() {
        cmbSuppliers.removeAllItems();
        for (Supplier s : supplierDirectory.getSupplierList()) {
        cmbSuppliers.addItem(s.getSupplierName()); // only names
        }
    }
    
    private void updateSupplierVisibility(){
        String selectedRole = (String) cmbRoles.getSelectedItem();
        boolean supplierRole = selectedRole != null && selectedRole.equals("Supplier");
        lblSupplier.setVisible(supplierRole);
        cmbSuppliers.setVisible(supplierRole);
        if (!supplierRole) selectedSupplier = null;
   }
