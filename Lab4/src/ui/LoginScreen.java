    private void cmbSuppliersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbSuppliersActionPerformed
        // TODO add your handling code here:
        String selectedName = (String) cmbSuppliers.getSelectedItem();
        if (selectedName != null) {
        selectedSupplier = supplierDirectory.getSupplierList()
                .stream()
                .filter(s -> s.getSupplierName().equals(selectedName))
                .findFirst()
                .orElse(null);
            }
    }//GEN-LAST:event_cmbSuppliersActionPerformed


