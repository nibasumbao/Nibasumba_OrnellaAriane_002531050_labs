    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        
                                               
        String selectedRole = (String) cmbRoles.getSelectedItem();

        if (selectedRole == null) {
        JOptionPane.showMessageDialog(this, "Please select a role", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
        }

        if (selectedRole.equals("Administrator")) {
        AdminWorkAreaJPanel adminPanel = new AdminWorkAreaJPanel(workArea, supplierDirectory);
        workArea.add("AdminWorkAreaJPanel", adminPanel);
        } 
        else if (selectedRole.equals("Supplier")) {
            if (selectedSupplier == null) {
            JOptionPane.showMessageDialog(this, "Please select a supplier", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        SupplierWorkAreaJPanel supplierPanel = new SupplierWorkAreaJPanel(workArea, selectedSupplier);
        workArea.add("SupplierWorkAreaJPanel", supplierPanel);
        }

        CardLayout layout = (CardLayout) workArea.getLayout();
        layout.next(workArea);
        

        
    }//GEN-LAST:event_btnLoginActionPerformed

